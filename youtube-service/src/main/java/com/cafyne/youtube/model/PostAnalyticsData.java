package com.cafyne.youtube.model;


public class PostAnalyticsData{
	
	double impact;
	double futureImpact;
	double scapital;
	double sentiment;

	
	public double getImpact() {
		return impact;
	}
	public void setImpact(double impact) {
		this.impact = impact;
	}
	public double getFutureImpact() {
		return futureImpact;
	}
	public void setFutureImpact(double futureImpact) {
		this.futureImpact = futureImpact;
	}
	public double getScapital() {
		return scapital;
	}
	public void setScapital(double scapital) {
		this.scapital = scapital;
	}
	public double getSentiment() {
		return sentiment;
	}
	public void setSentiment(double sentiment) {
		this.sentiment = sentiment;
	}


}
